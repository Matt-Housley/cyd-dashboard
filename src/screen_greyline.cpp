// ─── Scanline polygon fill + outline ─────────────────────────────────────────
static void fillPolygon(const int* px, const int* py, int n,
                        uint32_t fillCol, uint32_t edgeCol) {
    if (n < 3) return;
    int minY = py[0], maxY = py[0];
    for (int i = 1; i < n; i++) {
        if (py[i] < minY) minY = py[i];
        if (py[i] > maxY) maxY = py[i];
    }
    minY = max(minY, MAP_Y0);
    maxY = min(maxY, MAP_Y0 + MAP_H - 1);

    for (int y = minY; y <= maxY; y++) {
        int xs[96]; int nxs = 0;
        for (int i = 0; i < n && nxs < 94; i++) {
            int j = (i + 1) % n;
            int y0 = py[i], y1 = py[j];
            if (y0 == y1) continue;
            if ((y0 <= y && y < y1) || (y1 <= y && y < y0))
                xs[nxs++] = px[i] + (y - y0) * (px[j] - px[i]) / (y1 - y0);
        }
        for (int a = 1; a < nxs; a++) {
            int k = xs[a], b = a - 1;
            while (b >= 0 && xs[b] > k) { xs[b+1] = xs[b]; b--; }
            xs[b+1] = k;
        }
        for (int a = 0; a + 1 < nxs; a += 2)
            spr.drawFastHLine(xs[a], y, xs[a+1] - xs[a] + 1, fillCol);
    }
    for (int i = 0; i < n; i++) {
        int j = (i + 1) % n;
        int y0 = constrain(py[i], MAP_Y0, MAP_Y0 + MAP_H - 1);
        int y1 = constrain(py[j], MAP_Y0, MAP_Y0 + MAP_H - 1);
        spr.drawLine(px[i], y0, px[j], y1, edgeCol);
    }
}

